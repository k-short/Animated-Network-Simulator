public class Node {
    //Coordinates for the node
    private double xCoord;
    private double yCoord;

    public Node(double x, double y){
        xCoord = x;
        yCoord = y;
    }

    public double getXCoord() {
        return xCoord;
    }

    public double getYCoord() {
        return yCoord;
    }

}
